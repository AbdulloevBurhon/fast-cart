// Container.jsx
//
// ┌─────────────────────────────────────────────────────────┐
// │  МИН ШИРИНА:  320px  (2xsm breakpoint)                 │
// │  МАКС ШИРИНА: 1400px (все страницы ограничены этим)     │
// │                                                         │
// │  ПАДДИНГИ (одинаковые во всём проекте):                 │
// │   мобайл  (< 640px):   px-4  = 16px с каждой стороны   │
// │   планшет (640–1023px): px-6 = 24px с каждой стороны   │
// │   десктоп (1024px+):   px-8  = 32px с каждой стороны   │
// │                                                         │
// │  КАК ИСПОЛЬЗОВАТЬ:                                      │
// │  Каждая секция оборачивает контент в <Container>        │
// │  НЕ добавляй свои px-* внутри секций — они уже здесь   │
// └─────────────────────────────────────────────────────────┘

function Container({ children, className = '' }) {
 return (
  <div
   className={`
        w-full
        min-w-[320px]
        max-w-[1400px]
        mx-auto
        px-4 sm:px-6 lg:px-8
        ${className}
      `}
  >
   {children}
  </div>
 )
}

export default Container
